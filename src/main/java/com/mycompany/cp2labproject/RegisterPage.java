    private void addRegister(int ID, String name, String surname, int income) {
    String url = "jdbc:mysql://127.0.0.1:3306/fzt";
    String username = "root"; // Kullanıcı adını buraya girin
    String password = "fsmvu";

    try (Connection connection = DriverManager.getConnection(url, username, password);
         PreparedStatement checkStatement = connection.prepareStatement("SELECT id FROM register WHERE id = ?");
         PreparedStatement insertStatement = connection.prepareStatement("INSERT INTO register (id, name, surname, income) VALUES (?, ?, ?, ?)")) {

        // İlgili ID'ye sahip kaydı kontrol et
        checkStatement.setInt(1, ID);
        ResultSet resultSet = checkStatement.executeQuery();

        if (resultSet.next()) {
            JOptionPane.showMessageDialog(this, "Same id is using!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Eğer aynı ID'ye sahip kayıt yoksa yeni kaydı ekle
            insertStatement.setInt(1, ID);
            insertStatement.setString(2, name);
            insertStatement.setString(3, surname);
            insertStatement.setInt(4, income);

            int rowsAffected = insertStatement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Successful", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No rows were affected. Save operation failed.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
JOptionPane.showMessageDialog(this, "Error", "Success", JOptionPane.INFORMATION_MESSAGE);
    }
}

