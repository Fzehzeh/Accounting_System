    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
             try {
        String name = nametxt.getText();
        if (name.isEmpty()) {
            throw new IllegalArgumentException("İsim boş olamaz");
        }

        String IDStr = IDtxt.getText();
        if (IDStr.isEmpty()) {
            throw new IllegalArgumentException("ID boş olamaz");
        }

        String surname = surnametxt.getText();
        if (surname.isEmpty()) {
            throw new IllegalArgumentException("Soyisim boş olamaz");
        }

        if (name.matches(".*\\d.*") || surname.matches(".*\\d.*")) {
            throw new IllegalArgumentException("İsim veya soyisimde rakam bulunamaz");
        }

        int ID = Integer.parseInt(IDStr);
        
        String incomestr = incometxt.getText();
        if (incomestr.isEmpty()) {
            throw new IllegalArgumentException("Gelir boş olamaz");
        }

        int income = Integer.parseInt(incomestr);

        // Eğer tüm kontroller başarılıysa, işlemleri gerçekleştir
        addRegister(ID, name, surname, income);
    } catch (NumberFormatException e) {
        // Sayıya dönüştürme hatası
        e.printStackTrace();
    } catch (IllegalArgumentException e) {
        // Hatalı giriş durumu
        JOptionPane.showMessageDialog(this, e.getMessage(), "Hata", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnRegisterActionPerformed

