// Şirket verilerini tabloya yükleme metod
    private void loadCompanyData() {
        // Tablo sütunlarını ayarla
        companymodel.setColumnIdentifiers(new String[]{"ID", "Name", "Username", "Income", " Tax"});
        companymodel.setRowCount(0);

        try ( Connection connection = DriverManager.getConnection(url, username, password);  Statement statement = connection.createStatement();  ResultSet resultSet = statement.executeQuery("SELECT * FROM company")) {

            while (resultSet.next()) {
                 // Sonuç kümesinden verileri al
                String id = resultSet.getString("id");
                String name = resultSet.getString("name");
                String Username = resultSet.getString("username");
                String income = resultSet.getString("income");
                String tax = resultSet.getString("tax");
                // Verileri tablo modeline ekle
                companymodel.addRow(new Object[]{id, name, Username, income, tax});
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    // Şirket verilerini bir metin dosyasına yazma metod
    private void writeDataToTextFile() {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("company_data.txt"))) {
            // Başlık satırını yaz
            writer.write("ID\tName\tUsername\tIncome\tTax\n");
        // Verileri yazmak için satır ve sütunlarda döngü
            for (int i = 0; i < companymodel.getRowCount(); i++) {
                StringBuilder rowData = new StringBuilder();
                for (int j = 0; j < companymodel.getColumnCount(); j++) {
                    rowData.append(companymodel.getValueAt(i, j));
                    if (j < companymodel.getColumnCount() - 1) {
                        rowData.append("\t"); // Sutunlar arasına sekme karakteri ekler
                    }
                }
                writer.write(rowData.toString());
                writer.newLine(); // Yeni bir satıra geçer
            }
            
            System.out.println("Veriler başarıyla dosyaya yazıldı: company_data.txt");
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

