    public void deleteManager (int ID) {
        String sql = "DELETE FROM manager WHERE id = ?";

        try (Connection connection = DriverManager.getConnection(url, username, password);
             PreparedStatement preparedStatement = connection.prepareStatement(sql)) {

            preparedStatement.setInt(1, ID);

            int rowsDeleted = preparedStatement.executeUpdate();
            if (rowsDeleted > 0) {
                System.out.println("Manager with ID " + ID + " was deleted successfully!");
            } else {
                System.out.println("No manager found with ID " + ID);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
     public void findManagerByID(int ID) {
        String sql = "SELECT name, surname FROM manager WHERE id = ?";

        try (Connection connection = DriverManager.getConnection(url, username, password);
             PreparedStatement preparedStatement = connection.prepareStatement(sql)) {

            preparedStatement.setInt(1, ID);

            try (ResultSet resultSet = preparedStatement.executeQuery()) {
                if (resultSet.next()) {
                    String name = resultSet.getString("name");
                    String surname = resultSet.getString("surname");

                    String message = "Name: " + name + "\nSurname: " + surname + "\nID: " + ID;
                    JOptionPane.showMessageDialog(null, message, "Manager Information", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "There is no manager with this ID.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
  

