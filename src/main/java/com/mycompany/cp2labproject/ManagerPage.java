    private void writeSelectedRowToTextFile(int selectedRowIndex) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("selected_row.txt"))) {
            // Verileri JTable'dan al
            String id = (String) companymodel.getValueAt(selectedRowIndex, 0);
            String name = (String) companymodel.getValueAt(selectedRowIndex, 1);
            String username = (String) companymodel.getValueAt(selectedRowIndex, 2);
            String income = (String) companymodel.getValueAt(selectedRowIndex, 3);
            String tax = (String) companymodel.getValueAt(selectedRowIndex, 4);

            // Verileri dosyaya yaz
            writer.write("ID: " + id);
            writer.newLine();
            writer.write("Name: " + name);
            writer.newLine();
            writer.write("Username: " + username);
            writer.newLine();
            writer.write("Income: " + income);
            writer.newLine();
            writer.write("Tax: " + tax);
            writer.newLine();

            JOptionPane.showMessageDialog(null, "Seçilen satır başarıyla dosyaya yazıldı: selected_row.txt", "Bilgi", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Dosya yazma hatası oluştu.", "Hata", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    
    
